#include <iostream>
int main()
{
  int first = 10;
  int second = 20;
  int three = second + first;
  std::cout << 'second + first' << second + first << '\n';
  std::cout << 'second / first' << second / first << '\n';
  std::cout << 'second * first' << second * first << '\n';
  std::cout << 'second - first' << second - first << '\n';
  std::cout << 'three' << three;
}